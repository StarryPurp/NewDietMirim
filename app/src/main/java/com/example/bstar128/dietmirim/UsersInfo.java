package com.example.bstar128.dietmirim;

public class UsersInfo {
    private String name;
    private  String id;
    private  int height;
    private  int weight;
    private  String gender;
    private  int exercise_p;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public int getHeight() {
        return height;
    }

    public void setHeight(int height) {
        this.height = height;
    }

    public int getWeight() {
        return weight;
    }

    public void setWeight(int weight) {
        this.weight = weight;
    }

    public String getGender() {
        return gender;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }

    public int getExercise_p() {
        return exercise_p;
    }

    public void setExercise_p(int exercise_p) {
        this.exercise_p = exercise_p;
    }
}
